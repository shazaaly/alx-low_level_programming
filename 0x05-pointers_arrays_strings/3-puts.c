#include "main.h"
/**
* _puts - prints a string followed by a new line to stdout
* @str: a pointer to the string to be printed
*
* This function takes a pointer to a
* string and uses a loop to iterate over
*/
void _puts(char *str)
{
	while (*str != '\0')
	{
		_putchar(*str + 0);
		str++;
	}
	_putchar('\n');
}
